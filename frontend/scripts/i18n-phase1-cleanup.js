#!/usr/bin/env node

/**
 * i18n Phase 1 Cleanup Script
 *
 * 1. Removes dead keys from common.json and medical.json across all locales
 * 2. Removes exact duplicate keys from common.json that already exist in medical.json
 *
 * Run with --dry-run to preview changes without writing files.
 * Run without flags to apply changes.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const LOCALES_DIR = path.join(__dirname, '..', 'public', 'locales');
const ALL_LOCALES = ['en', 'de', 'es', 'fr', 'it', 'nl', 'pl', 'pt', 'ru', 'sv'];
const DRY_RUN = process.argv.includes('--dry-run');

// ─── Dead Keys ──────────────────────────────────────────────────────
// Loaded from unused-keys-data.json (generated by check-exposed-keys.js --unused).
// Only common and medical namespaces. Excludes notifications, errors, navigation.

const keysData = JSON.parse(
  fs.readFileSync(path.join(__dirname, 'unused-keys-data.json'), 'utf8')
);
const UNUSED_COMMON_KEYS = keysData.common;
const UNUSED_MEDICAL_KEYS = keysData.medical;

// ─── Keys to KEEP (false positives) ────────────────────────────────
// Dynamic keys accessed via template literals or iteration
const KEEP_PATTERNS = [
  /^searchPlaceholders\./,
  /^statusDescriptions\./,
  /^exportPage\.scopes\./,
  /^reportBuilder\.categories\./,
  /^severity\./,
  /^status\./,
  /^vitals\.form\.validation\./,
  /^vitals\.units\./,
  /^vitals\.categories\./,
  /_one$/,
  /_other$/,
];

function shouldKeep(key) {
  return KEEP_PATTERNS.some(p => p.test(key));
}

// ─── Helpers ────────────────────────────────────────────────────────

function flatten(obj, prefix = '') {
  const result = {};
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? `${prefix}.${k}` : k;
    if (typeof v === 'object' && v !== null) {
      Object.assign(result, flatten(v, key));
    } else {
      result[key] = v;
    }
  }
  return result;
}

function deleteKeyPath(obj, keyPath) {
  const parts = keyPath.split('.');
  let current = obj;
  const stack = [];

  for (let i = 0; i < parts.length - 1; i++) {
    if (current[parts[i]] === undefined) return false;
    stack.push({ obj: current, key: parts[i] });
    current = current[parts[i]];
  }

  const lastKey = parts[parts.length - 1];
  if (current[lastKey] === undefined) return false;

  delete current[lastKey];

  // Clean up empty parent objects
  for (let i = stack.length - 1; i >= 0; i--) {
    const { obj: parent, key } = stack[i];
    if (Object.keys(parent[key]).length === 0) {
      delete parent[key];
    } else {
      break;
    }
  }

  return true;
}

function loadJSON(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function writeJSON(filePath, data) {
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
}

// ─── Part 1: Remove Dead Keys ───────────────────────────────────────

function removeDeadKeys() {
  const commonToRemove = UNUSED_COMMON_KEYS.filter(k => !shouldKeep(k));
  const medicalToRemove = UNUSED_MEDICAL_KEYS.filter(k => !shouldKeep(k));

  console.log(`\nPart 1: Dead Key Removal`);
  console.log(`  common keys to remove: ${commonToRemove.length} (${UNUSED_COMMON_KEYS.length} unused - ${UNUSED_COMMON_KEYS.length - commonToRemove.length} kept as dynamic/plural)`);
  console.log(`  medical keys to remove: ${medicalToRemove.length} (${UNUSED_MEDICAL_KEYS.length} unused - ${UNUSED_MEDICAL_KEYS.length - medicalToRemove.length} kept as dynamic/plural)`);

  let totalRemoved = 0;

  for (const locale of ALL_LOCALES) {
    // common.json
    const commonPath = path.join(LOCALES_DIR, locale, 'common.json');
    if (fs.existsSync(commonPath)) {
      const data = loadJSON(commonPath);
      let removed = 0;
      for (const key of commonToRemove) {
        if (deleteKeyPath(data, key)) removed++;
      }
      if (!DRY_RUN) writeJSON(commonPath, data);
      console.log(`  ${locale}/common.json: ${removed} keys removed`);
      totalRemoved += removed;
    }

    // medical.json
    const medicalPath = path.join(LOCALES_DIR, locale, 'medical.json');
    if (fs.existsSync(medicalPath)) {
      const data = loadJSON(medicalPath);
      let removed = 0;
      for (const key of medicalToRemove) {
        if (deleteKeyPath(data, key)) removed++;
      }
      if (!DRY_RUN) writeJSON(medicalPath, data);
      console.log(`  ${locale}/medical.json: ${removed} keys removed`);
      totalRemoved += removed;
    }
  }

  return totalRemoved;
}

// ─── Part 2: Remove Exact Duplicates ────────────────────────────────

function removeDuplicates() {
  console.log(`\nPart 2: Duplicate Removal (common keys that exist identically in medical)`);

  // Find duplicates using EN as reference
  const enCommon = loadJSON(path.join(LOCALES_DIR, 'en', 'common.json'));
  const enMedical = loadJSON(path.join(LOCALES_DIR, 'en', 'medical.json'));

  const flatCommon = flatten(enCommon);
  const flatMedical = flatten(enMedical);

  const duplicateKeys = [];
  for (const [key, value] of Object.entries(flatCommon)) {
    if (flatMedical[key] !== undefined && flatMedical[key] === value) {
      duplicateKeys.push(key);
    }
  }

  console.log(`  Found ${duplicateKeys.length} exact duplicates in EN`);

  // Remove from common since medical is the more specific namespace.
  // Components use useTranslation(['medical', 'common']) which checks medical first.

  let totalRemoved = 0;

  for (const locale of ALL_LOCALES) {
    const commonPath = path.join(LOCALES_DIR, locale, 'common.json');
    const medicalPath = path.join(LOCALES_DIR, locale, 'medical.json');

    if (!fs.existsSync(commonPath) || !fs.existsSync(medicalPath)) continue;

    const commonData = loadJSON(commonPath);
    const medicalData = loadJSON(medicalPath);
    const flatMed = flatten(medicalData);

    let removed = 0;
    for (const key of duplicateKeys) {
      // Only remove from common if the key still exists in this locale's medical.json
      if (flatMed[key] !== undefined) {
        if (deleteKeyPath(commonData, key)) removed++;
      }
    }

    if (!DRY_RUN) writeJSON(commonPath, commonData);
    console.log(`  ${locale}/common.json: ${removed} duplicate keys removed`);
    totalRemoved += removed;
  }

  return { totalRemoved, duplicateKeys };
}

// ─── Main ───────────────────────────────────────────────────────────

console.log(`i18n Phase 1 Cleanup${DRY_RUN ? ' (DRY RUN)' : ''}`);
console.log('='.repeat(50));

const deadRemoved = removeDeadKeys();
const { totalRemoved: dupeRemoved, duplicateKeys } = removeDuplicates();

console.log(`\n${'='.repeat(50)}`);
console.log(`Summary:`);
console.log(`  Dead keys removed: ${deadRemoved} (across ${ALL_LOCALES.length} locales)`);
console.log(`  Duplicate keys removed: ${dupeRemoved} (across ${ALL_LOCALES.length} locales)`);
console.log(`  Total key removals: ${deadRemoved + dupeRemoved}`);
if (DRY_RUN) console.log(`\n  (DRY RUN - no files were modified)`);
