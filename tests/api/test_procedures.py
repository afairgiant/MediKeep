        update_data = {
            "status": "completed",
            "outcome": "successful",
        }

        response = client.put(
            f"/api/v1/procedures/{procedure_id}",
            json=update_data,
            headers=authenticated_headers
        )

        assert response.status_code == 200
        data = response.json()
        assert data["outcome"] == "successful"
        assert data["status"] == "completed"

